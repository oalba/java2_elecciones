public class EspacioPublico {
	private int extension;
	private String direccion;

	public void setExtension (int ex){
		extension = ex;
	}
	public int getExtension(){
		return extension;
	}

	public void setDireccion (String dir){
		direccion = dir;
	}
	public String getDireccion(){
		return direccion;
	}
}